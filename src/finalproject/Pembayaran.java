    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int bayar ;
        String id = "";
        bayar= Integer.parseInt(cashPembeli.getText());
        sisa = bayar - jumlah;
        if(sisa>=0){
            this.kembalian.setText("Kembalian : Rp."+sisa);
            try{
                Connection c = Koneksi.getKoneksi();
                ResultSet r = c.createStatement().executeQuery("select * from pembeli where keterangan = 'Belum Lunas'");
                while(r.next()){
                    if(id==""){
                        id = r.getString(1);
                    }
                }
                File file = new File("src/finalproject/cetak.jrxml");
                jasperDesign = JRXmlLoader.load(file);
                param.put("id", id);
                jasperReport = JasperCompileManager.compileReport(jasperDesign);
                jasperPrint = JasperFillManager.fillReport(jasperReport, param, c);
                JasperViewer.viewReport(jasperPrint, false);
                c.createStatement().executeUpdate("update pembeli set keterangan='Lunas' where  ID='"+id+"'");
                Monitor m = new Monitor();
                m.setVisible(true);
                dispose();
            } catch (SQLException ex){
                Logger.getLogger(Pembayaran.class.getName()).log(Level.SEVERE, null, ex);
            } catch (JRException ex) {
                Logger.getLogger(Pembayaran.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Uang tidak cukup");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

