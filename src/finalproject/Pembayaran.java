    private void tampilkan() {
        int row = TabelPembeli.getRowCount();
        String id = "";
        for(int a=0;a<row;a++){
            model1.removeRow(0);
        }
        try{
            Connection c = Koneksi.getKoneksi();
            ResultSet r = c.createStatement().executeQuery("select * from pembeli where keterangan = 'Belum Lunas'");
            while(r.next()){
                if(id==""){
                    id = r.getString(1);
                }
                if(id.equals(r.getString(1))){
                    int j = (Integer.parseInt(r.getString(4)))*(Integer.parseInt(r.getString(5)));
                    String data [] = {r.getString(1),r.getString(2),r.getString(3),r.getString(4),r.getString(5), Integer.toString(j)};
                    model1.addRow(data);
                    jumlah = jumlah + j;
                }
            }
        } catch (SQLException ex){
            Logger.getLogger(Pembayaran.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.labelPembeli.setText("ID Customer : " + id);
        this.labelTotal.setText("Total : Rp. " + jumlah);
    }

